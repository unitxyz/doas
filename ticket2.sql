-- 1. Знайти клієнтів, які зробили замовлення на певний товар більше ніж один раз (у різних замовленнях). Вивести ім'я, прізвище клієнта та найпопулярніший товар серед них.
SELECT
c.first_name, -- Ім'я клієнта
c.last_name, -- Прізвище клієнта
o.item -- Товар, який замовляли кілька разів
FROM Customers c -- Таблиця клієнтів
INNER JOIN Orders o -- Зв'язуємо з замовленнями
ON c.customer_id = o.customer_id -- Зв'язок: клієнт = замовлення
GROUP BY
c.customer_id, -- Групуємо по клієнту
c.first_name, -- і по імені
c.last_name, -- і по прізвищу
o.item -- і по товару
HAVING COUNT(o.item) > 1 -- Фільтр: товар зустрічається більше 1 разу у цього клієнта
ORDER BY COUNT(o.item) DESC -- Сортуємо: найпопулярніший товар — першим
LIMIT 1; -- Беремо тільки найпопулярніший


-- 2. Знайти всі замовлення для клієнтів з певної країни (наприклад, "UK"), де товарів було замовлено більше ніж на 700 одиниць. Вивести ідентифікатор замовлення, ім'я, прізвище клієнта та загальну кількість товарів.
SELECT
o.order_id, -- ID замовлення
c.first_name, -- Ім'я клієнта
c.last_name, -- Прізвище клієнта
o.amount -- Кількість товарів (>700)
FROM Orders o -- Таблиця замовлень (основна)
INNER JOIN Customers c -- Зв'язуємо з клієнтами
ON o.customer_id = c.customer_id -- Зв'язок: замовлення = клієнт
WHERE
c.country = 'UK' -- Умова 1: клієнт з UK
AND o.amount > 700; -- Умова 2: більше 700 одиниць


-- 3. Порахувати середній вік клієнтів, які мають хоча б одне замовлення. Вивести цей середній вік.
SELECT
AVG(c.age) AS avg_age -- Середній вік клієнтів з замовленнями
FROM Customers c -- Таблиця клієнтів
WHERE c.customer_id IN ( -- Фільтр: тільки ті, хто є в замовленнях
    SELECT DISTINCT customer_id -- Унікальні ID клієнтів
    FROM Orders -- З таблиці замовлень
);


-- 4. Знайти кількість доставок для кожного клієнта. Вивести ідентифікатор клієнта, ім'я, прізвище та кількість таких доставок.
SELECT
c.customer_id, -- ID клієнта
c.first_name, -- Ім'я клієнта
c.last_name, -- Прізвище клієнта
COUNT(s.shipping_id) AS delivery_count -- Кількість доставок цього клієнта
FROM Customers c -- Таблиця клієнтів (основна)
INNER JOIN Shippings s -- Зв'язуємо з доставками
ON c.customer_id = s.customer -- Зв'язок: клієнт = доставка
GROUP BY
c.customer_id, -- Групуємо по ID клієнта
c.first_name, -- і по імені
c.last_name; -- і по прізвищу


-- 5. Знайти замовлення, що містять товар, якого не замовляли інші клієнти. Вивести ідентифікатор замовлення, товар та кількість одиниць.
SELECT
o.order_id, -- ID замовлення
o.item, -- Назва унікального товару
o.amount -- Кількість одиниць
FROM Orders o -- Таблиця замовлень
WHERE o.item IN ( -- Фільтр: товар входить до списку "унікальних"
    SELECT item -- Назва товару
    FROM Orders -- З таблиці замовлень
    GROUP BY item -- Групуємо по товару
    HAVING COUNT(DISTINCT customer_id) = 1 -- Умова: товар замовляв лише 1 клієнт
);
