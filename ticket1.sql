-- 1. Знайти всіх клієнтів, які зробили замовлення та мають статус доставки "в обробці". Вивести імена та прізвища таких клієнтів.
SELECT DISTINCT 
    c.first_name, -- Ім'я клієнта
    c.last_name -- Прізвище клієнта
FROM Customers c -- Таблиця клієнтів (основна)
INNER JOIN Orders o -- Зв'язуємо з заказами
    ON c.customer_id = o.customer_id -- Зв'язок: клієнт = замовлення
INNER JOIN Shippings s -- Зв'язуємо з доставками
    ON c.customer_id = s.customer -- Зв'язок: клієнт = доставка
WHERE s.status = 'Pending'; -- Фільтр: статус "в обробці"

-- 2. Знайти клієнтів, в яких кількість товару перевищує 400 одиниць. Вивести їх ідентифікатор, ім'я, прізвище та загальну кількість замовлених товарів.
SELECT DISTINCT 
    c.customer_id, -- ID клієнта
    c.first_name, -- Ім'я клієнта
    c.last_name, -- Прізвище клієнта
    o.amount -- Кількість товару (>400)
FROM Customers c -- Таблиця клієнтів
INNER JOIN Orders o -- Зв'язуємо з заказами
    ON c.customer_id = o.customer_id -- Зв'язок: клієнт = замовлення
WHERE o.amount > 400; -- Фільтр: більше 400 одиниць

-- 3. Знайти 2 найбільш популярних товари серед усіх замовлень. Вивести назву товару та кількість одиниць кожного товару.
SELECT 
    item, -- Назва товару
    amount -- Кількість одиниць
FROM Orders -- Таблиця замовлень
ORDER BY amount DESC -- Сортуємо за кількістю (найбільше → найменше)
LIMIT 2; -- Беремо тільки перші 2

-- 4. Знайти найстаршого клієнта в кожній країні. Вивести країну, ім'я, прізвище та вік цього клієнта.
SELECT 
    c.country, -- Країна
    c.first_name, -- Ім'я
    c.last_name, -- Прізвище
    c.age -- Вік
FROM Customers c -- Всіх клієнтів
INNER JOIN ( -- Зв'язуємо з підзапросом:
    SELECT 
        country, -- Країна
        MAX(age) as max_age -- Максимальний вік у цій країні
    FROM Customers -- З таблиці клієнтів
    GROUP BY country -- Групуємо по країнах
) max_c -- Називаємо цей результат max_c
    ON c.country = max_c.country -- Умова 1: та сама країна
    AND c.age = max_c.max_age; -- Умова 2: вік = максимальному

-- 5. Знайти всі замовлення, які були здійснені клієнтами старше 30 років і мали статус доставки "доставлено". Вивести ідентифікатор замовлення, товар, кількість одиниць і статус доставки.
SELECT 
    o.order_id, -- ID замовлення
    o.item, -- Назва товару
    o.amount, -- Кількість одиниць
    s.status -- Статус доставки
FROM Orders o -- Таблиця замовлень (основна)
INNER JOIN Customers c -- Зв'язуємо з клієнтами
    ON o.customer_id = c.customer_id -- Зв'язок: замовлення = клієнт
INNER JOIN Shippings s -- Зв'язуємо з доставками
    ON o.customer_id = s.customer -- Зв'язок: клієнт = доставка
WHERE 
    c.age > 30 -- Умова 1: клієнт старше 30 років
    AND s.status = 'Delivered'; -- Умова 2: доставка "доставлено"
